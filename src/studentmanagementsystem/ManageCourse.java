    private void jTableCourseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCourseMouseClicked
        int index = jTableCourse.getSelectedRow();
        jTextField_courseId.setText(jTableCourse.getValueAt(index, 0).toString());
        jTextField_courseName.setText(jTableCourse.getValueAt(index, 1).toString());
    }//GEN-LAST:event_jTableCourseMouseClicked

    private void jButton_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateActionPerformed
            if(!c.isCourseExist(jTextField_courseName.getText())){
            String cname = jTextField_courseName.getText();
            int id = Integer.valueOf(jTextField_courseId.getText());
            c.operation('u',id, cname);
            
            jTableCourse.setModel(new DefaultTableModel(null, new Object[]{"Course ID","Course Name"}));
            c.fillCourseTable(jTableCourse);
            
            
        } else{
            JOptionPane.showMessageDialog(null, "Course already Exists!");
        }
    }//GEN-LAST:event_jButton_updateActionPerformed

