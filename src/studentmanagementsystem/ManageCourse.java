    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        int id = Integer.valueOf(jTextField_courseId.getText());
        c.operation('d', id, null);
        jTableCourse.setModel(new DefaultTableModel(null, new Object[]{"Course Id","COurse Name"}));
        c.fillCourseTable(jTableCourse);
        jTextField_courseName.setText("");
        jTextField_courseId.setText("");
        
        
        
     }//GEN-LAST:event_jButton_deleteActionPerformed

    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        if(!c.isCourseExist(jTextField_courseName.getText())){
            String cname = jTextField_courseName.getText();
            c.operation('i',null, cname);
            
            jTableCourse.setModel(new DefaultTableModel(null, new Object[]{"Course ID","Course Name"}));
            c.fillCourseTable(jTableCourse);
            
        } else{
            JOptionPane.showMessageDialog(null, "Course already Exists!");
        }
    
    }//GEN-LAST:event_jButton_addActionPerformed

